package com.prokarma.ai.data.exception;

public class DataloadException extends Exception {

	public DataloadException() {
		super();
	}

	public DataloadException(String arg0) {
		super(arg0);
		
	}

	public DataloadException(Throwable arg0) {
		super(arg0);
		
	}

	public DataloadException(String arg0, Throwable arg1) {
		super(arg0, arg1);
		
	}

	public DataloadException(String arg0, Throwable arg1, boolean arg2, boolean arg3) {
		super(arg0, arg1, arg2, arg3);
		
	}

}
